public class Main {
    public static void main(String[] args) {
        int angka = 1;   // mulai dari 1

        System.out.println(angka);   // 1
        angka = angka * 10;
        System.out.println(angka);   // 10
        angka = angka * 10;
        System.out.println(angka);   // 100
        angka = angka * 10;
        System.out.println(angka);   // 1000
        angka = angka * 10;
        System.out.println(angka);   // 10000
        angka = angka * 10;
        System.out.println(angka);   // 100000
        angka = angka * 10;
        System.out.println(angka);   // 1000000
    }
}
